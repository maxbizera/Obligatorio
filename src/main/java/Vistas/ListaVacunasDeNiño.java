    public ListaVacunasDeNiño(AdministradorDeNiños administradorDeNiño, String documento) {
        
        this.administradorDeNiños = administradorDeNiño;
        this.documento = documento;

        this.vacunas = new Object[this.administradorDeNiños.listar().size()][5];

        int index = 0;
        for (ResumenNiño niño : this.administradorDeNiños.listar()) {
            this.vacunas[index][0] = niño.getNombre();
            this.vacunas[index][1] = niño.getDocumento();
            this.vacunas[index][2] = niño.getServicioDeSalud();
            this.vacunas[index][2] = niño.getEdad();
            ++index;
        }
        
        setTitle("Lista de vacunas de: " + this.vacunas[0][0]);

        initComponents();
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        setLocation(dim.width / 2 - getSize().width / 2, dim.height / 2 - getSize().height / 2);

        jTableListaConsultas.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent event) {
                idSelected = jTableListaConsultas.getValueAt(jTableListaConsultas.getSelectedRow(), 1).toString();
                jMenuItemEliminarVacuna.setEnabled(true);
                jMenuItemModificarVacuna.setEnabled(true);
            }
        });
    }

